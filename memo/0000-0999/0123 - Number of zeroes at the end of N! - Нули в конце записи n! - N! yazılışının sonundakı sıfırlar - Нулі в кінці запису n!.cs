using System;

namespace Problem123
{
    internal class Program
    {
        public static void Main(string[] args)
        {
            long n = long.Parse(Console.ReadLine());
            long p = 5;
            long say = 0;
            while (n/p != 0)
            {
                say = say + n/p;
                p = p*5;
            }
            Console.WriteLine(say);
        }
    }
}
