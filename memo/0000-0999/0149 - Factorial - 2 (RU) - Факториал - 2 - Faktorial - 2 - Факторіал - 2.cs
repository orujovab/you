using System;

namespace Problem149
{
    internal class Program
    {
        public static void Main(string[] args)
        {
            long n = long.Parse(Console.ReadLine());
            double cem = 0;
            for (int i = 1; i <= n; i++)
            {
                cem = cem + Math.Log10(i);
            }
            Console.WriteLine((int) cem + 1);
        }
    }
}
